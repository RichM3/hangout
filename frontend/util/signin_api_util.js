export const signup = (userCredentials) => {
  return $.ajax({
    method: "",
    url: "",
    data: {userCredentials}
  })
}

export const login = (userCredentials) => {
  return $.ajax({
    method: "",
    url: "",
    data: {userCredentials}
  })
}

export const logout = (userCredentials) => {
  return $.ajax({
    method: "",
    url: ""
  })
}
